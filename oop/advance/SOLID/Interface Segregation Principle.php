/***
 * The Interface Segregation Principle suggests that clients should not be forced to depend on interfaces they do not use.
 * In essence, it is better to have multiple specific interfaces than one large, general-purpose interface.
 * This principle states that an interface should not enforce unwanted methods to a class.
 * The idea here is instead of having a large interface, we should have smaller interfaces.
 * So we should not create an interface having a lot of methods If we have such an interface, we should break in into smaller interfaces.
 * “Many client-specific interfaces are better than one general-purpose interface.”
 */